use cli_test_dir::*;

const BIN: &'static str = "./main";

#[test]
fn sample1() {
    let testdir = TestDir::new(BIN, "");
    let output = testdir
        .cmd()
        .output_with_stdin(r#"6 7
1 1 2
1 2 3
1 3 4
1 1 5
1 5 6
3 1
2 6
"#)
        .tee_output()
        .expect_success();
    assert_eq!(output.stdout_str(), r#"NYYNYY
NNYNNN
NNNYNN
NNNNNN
NNNNNY
YNNNYN"#);
    assert!(output.stderr_str().is_empty());
}

